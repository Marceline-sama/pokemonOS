class Pokemons:
    def __int__(self, nome, tipo1, especie, abilities1, gender, hp, attack, defense, spattack, spdef, speed):
        self.nome = nome
        self.tipo1 = tipo1
        self.especie = especie
        self.abilities1 = abilities1
        self.gender = gender
        self.hp = hp
        self.attack = attack
        self.defense = defense
        self.spattack = spattack
        self.spdef = spdef
        self.speed = speed

    def __init__(self, nome, tipo1, tipo2, especie, abilities1, abilities2, gender, hp, attack, defense, spattack, spdef, speed):
        self.nome = nome
        self.tipo1 = tipo1
        self.tipo2 = tipo2
        self.especie = especie
        self.abilities1 = abilities1
        self.abilities2 = abilities2
        self.gender = gender
        self.hp = hp
        self.attack = attack
        self.defense = defense
        self.spattack = spattack
        self.spdef = spdef
        self.speed = speed
